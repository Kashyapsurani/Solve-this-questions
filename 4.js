var mark = 300;

if(mark >= 0 && mark <= 90 ){
    console.log("First Quadrant")
}else if (mark >= 80 && mark <= 180 ){
    console.log("Second Quadrant")
}else if (mark >= 180 && mark <= 270 ){
    console.log("Third Quadrant")
}else if (mark >= 270 && mark <= 360){
    console.log("Fourth Quadrant")
}